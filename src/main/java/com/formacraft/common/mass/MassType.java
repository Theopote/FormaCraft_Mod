package com.formacraft.common.mass;

/**
 * MassType（体量类型）
 * <p>
 * 体量的"填充方式"，这是后续"派生真实结构"的关键
 * <p>
 * ⚠️ 注意：
 * - 这不是几何形状
 * - 这是"如何填充这个区域"的规则
 */
public enum MassType {
    /**
     * 实体体量（SOLID）
     * <p>
     * 用途：主体建筑
     * <p>
     * 规则：区域内允许放置方块
     */
    SOLID,

    /**
     * 中空体量（HOLLOW）
     * <p>
     * 用途：回廊、空洞、中庭
     * <p>
     * 规则：区域内不放置方块（但边界可能生成墙）
     */
    HOLLOW,

    /**
     * 板状体量（SLAB）
     * <p>
     * 用途：平台、屋盖、悬挑板
     * <p>
     * 规则：只在顶部/底部薄层放置方块
     */
    SLAB,

    /**
     * 框架体量（FRAME）
     * <p>
     * 用途：柱梁系统（v2）
     * <p>
     * 规则：只在结构线（柱、梁）位置放置方块
     */
    FRAME
}
